#include <stddef.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	register size_t	i;
	unsigned char	*tmp1;
	unsigned char	*tmp2;

	tmp1 = (unsigned char *)s1;
	tmp2 = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if (tmp1[i] == tmp2[i] && tmp1[i])
			i++;
		return (tmp1[i] == tmp2[i]);
	}
	return (0);
}
	